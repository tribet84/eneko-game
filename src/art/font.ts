// Map accented characters (es/ca) onto their base glyph for now (until accent
// glyphs are authored), so dialogue text never renders as '?'.
// Uppercase accented letters fall back to their base (rare at word-start in our
// text); lowercase accents now have real glyphs above.
const ACCENTS: Record<string, string> = {
  Á: 'A', À: 'A', É: 'E', È: 'E', Í: 'I', Ó: 'O', Ò: 'O', Ú: 'U', Ü: 'U',
  Ñ: 'N', Ç: 'C',
};

function glyph(ch: string): string[] {
  return F[ch] || F[ACCENTS[ch]] || F['?'];
}

export function textWidth(s: string, scale = 1, spacing = 1): number {
  let w = 0;
  for (const ch of s) w += (ch === ' ' ? 3 : GW + spacing) * scale;
  return w;
}

// Draw crisp pixel text. (x,y) = top-left. `shadow` (if given) is drawn 1 pixel
// down-right first, for the outlined SCUMM look.
export function drawText(
  ctx: CanvasRenderingContext2D,
  s: string,
  x: number,
  y: number,
  color: RGB,
  scale = 1,
  shadow: RGB | null = P.black,
  spacing = 1,
) {
  // Full 8-direction outline (not just a drop shadow) so text stays legible on
  // any background, then the fill colour on top.
  const o = scale;
  const passes: Array<[RGB, number, number]> = [];
  if (shadow) {
    passes.push(
      [shadow, -o, -o], [shadow, 0, -o], [shadow, o, -o],
      [shadow, -o, 0], [shadow, o, 0],
      [shadow, -o, o], [shadow, 0, o], [shadow, o, o],
    );
  }
  passes.push([color, 0, 0]);
  for (const [col, ox, oy] of passes) {
    ctx.fillStyle = css(col);
    let cx = x;
    for (const ch of s) {
      if (ch === ' ') { cx += 3 * scale; continue; }
      const g = glyph(ch);
      for (let ry = 0; ry < g.length; ry++) {
        const row = g[ry];
        for (let rx = 0; rx < GW; rx++) {
          if (row[rx] === '#') ctx.fillRect(cx + rx * scale + ox, y + ry * scale + oy, scale, scale);
        }
      }
      cx += (GW + spacing) * scale;
    }
  }
}
